    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear the table before populating
        int idCounter = 1; // Counter for the ID starting from 1

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM medicine ORDER BY medicine_pk"); // Assuming medicine_pk is the primary key

            while (rs.next()) {
                model.addRow(new Object[]{
                    idCounter, // Use the counter value instead of the database ID
                    rs.getString("uniqueId"),
                    rs.getString("name"),
                    rs.getString("companyName"),
                    rs.getString("quantity"),
                    rs.getString("price")
                });
                idCounter++; // Increment the counter for the next row
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        Object idObject = model.getValueAt(index, 0); // Assuming the ID is in the first column

        if (idObject != null) {
            String id = idObject.toString();

            int a = JOptionPane.showConfirmDialog(null, "Do you want to delete this medicine?", "Select", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                try {
                    Connection con = ConnectionProvider.getCon();
                    PreparedStatement ps = con.prepareStatement("delete from medicine where medicine_pk=?");
                    ps.setString(1, id);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Medicine successfully deleted");
                    setVisible(false);
                    new ViewMedicine().setVisible(true);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selected row contains null values");
        }
    }//GEN-LAST:event_jTable1MouseClicked

